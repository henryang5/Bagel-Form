    partial class AboutForm : Form
    {
        public AboutForm()
        {
            InitializeComponent();
            this.Text = "Bagel About"; 
            this.labelProductName.Text = "Bobby Beddar Bagel About Box";
            this.labelVersion.Text = String.Format("Version {0}", AssemblyVersion);
            this.labelCopyright.Text = AssemblyCopyright;
            this.labelCompanyName.Text = "Written By Henry Ang: SPU";
            this.textBoxDescription.Text = "Bobby Beddar Bagel form allows the user to maintain a list of bagel types. " + 
